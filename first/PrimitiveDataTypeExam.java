package first;
// 기본형 타입

public class PrimitiveDataTypeExam {

	public static void main(String[] args) {
		//boolean type
		boolean isFun = true;
		System.out.println(isFun);
		
		//char type, 문자 하나만 표현 
		char c = 'f';
		
		int x = 59;
		
		// int보다 큰 정수 표현 long, 마지막에 대문자 L or 소문자 l 붙여줌
		long bing = 123465465L;
		
		
		// 실수 float, 마지막에 대문자 F or 소문자 f 붙여줌
		float f = 32.4f;
				
		//더 큰 실수 표현 double
		double d = 3.16541;
		
		
	}

}
